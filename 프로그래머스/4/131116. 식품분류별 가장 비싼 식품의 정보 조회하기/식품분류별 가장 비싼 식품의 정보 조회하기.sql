WITH SUB AS (
    SELECT CATEGORY, MAX(PRICE) AS MAX_P
    FROM FOOD_PRODUCT
    WHERE CATEGORY IN ('과자', '국', '김치', '식용유')
    GROUP BY CATEGORY
)

SELECT P.CATEGORY, P.PRICE AS MAX_PRICE, P.PRODUCT_NAME
FROM FOOD_PRODUCT P
JOIN SUB S
ON (P.CATEGORY = S.CATEGORY)
    AND (P.PRICE = S.MAX_P)
ORDER BY MAX_PRICE DESC;
    