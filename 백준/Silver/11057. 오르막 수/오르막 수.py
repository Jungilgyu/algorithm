input = sys.stdin.readline

n = int(input())


dp = [[0] * (1001) for _ in range(1001)]

# dp[i][j] = i자리수의 j번째 값이 0~9일때 가지수

for i in range(1001):
    for j in range(1001):
        if i == 0 or j == 0:
            dp[i][j] = 1

        else:
            dp[i][j] = dp[i-1][j] + dp[i][j-1]

